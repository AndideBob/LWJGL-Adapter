package lwjgladapter.physics.collision.exceptions;

import lwjgladapter.datatypes.LWJGLAdapterException;

public class DublicatePhysicsHelperException extends LWJGLAdapterException {
	

	/**
	 * 
	 */
	private static final long serialVersionUID = 3325840821542713447L;

	public DublicatePhysicsHelperException(String message) {
		super(message);
	}

	public DublicatePhysicsHelperException(Throwable cause) {
		super(cause);
	}

	public DublicatePhysicsHelperException(String message, Throwable cause) {
		super(message, cause);
	}

	public DublicatePhysicsHelperException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
