package lwjgladapter.input;

import lwjgladapter.datatypes.LWJGLAdapterException;

public class DublicateInputManagerException extends LWJGLAdapterException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7242256649319995185L;

	public DublicateInputManagerException(String message) {
		super(message);
	}

	public DublicateInputManagerException(Throwable cause) {
		super(cause);
	}

	public DublicateInputManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public DublicateInputManagerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
