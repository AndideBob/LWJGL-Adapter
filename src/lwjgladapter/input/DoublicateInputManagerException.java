package lwjgladapter.input;

public class DoublicateInputManagerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7242256649319995185L;

	public DoublicateInputManagerException(String message) {
		super(message);
	}

	public DoublicateInputManagerException(Throwable cause) {
		super(cause);
	}

	public DoublicateInputManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public DoublicateInputManagerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
